    $startingLocation = Get-Location
    set-location $module.modulebase 
    Set-Location ..\
    $currentLocation = Get-Location 
    Set-Location ..\
    Remove-Item -Path $currentLocation -Recurse -Force
    set-location $startingLocation
}

